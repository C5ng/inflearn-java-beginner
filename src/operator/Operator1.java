package operator;

public class Operator1 {
    public static void main(String[] args) {
        int a = 5;
        int b = 2;

        int sum = a + b; // 덧셈
        System.out.println(sum);

        int diff = a - b; // 뺄셈
        System.out.println(diff);

        int multi = a * b; // 덧셈
        System.out.println(multi);

        int div = a / b;
        System.out.println(div); // 나눗셈

        int mod = a % b;
        System.out.println(mod); // 나머지
    }
}
